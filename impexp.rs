fn impexp(){
  let impint = 25;
  let impchar = "Hello World!";
  let expint: i64 = 27;
  let expchar: f32 = "Programming";
  println!("implicit int : {}",impint);
  println!("implicit char : {}",impchar);
  println!("explicit int(i64) : {}",expint);
  println!("explicit char(f32) : {}",expchar);
}
